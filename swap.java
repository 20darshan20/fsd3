public class swap {
    public static void main(String[]args){
        int first =20;
        int second =30;
        System.out.println("Before swap ");
        System.out.println("first value "+ first);
        System.out.println("second value "+ second);
        int temp = first;
        first=second;
        second=temp;
        System.out.println("After swap ");
        System.out.println("first value "+ first);
        System.out.println("second value "+ second);

    }

}
